# Các loại lỗi khi lập trình:
# 1. Lỗi cú pháp (Syntax Error): Xảy ra khi mã nguồn không tuân thủ các quy tắc cú pháp của ngôn ngữ lập trình.
# 2. Lỗi thời gian chạy (Runtime Error): Xảy ra khi chương trình đang chạy và gặp phải một tình huống không thể xử lý được, như chia cho số 0.
# 3. Lỗi logic (Logical Error): Xảy ra khi chương trình chạy mà không tạo ra kết quả đúng như mong đợi do lỗi trong logic của mã nguồn.
# 4. Lỗi ngoại lệ (Exception): Xảy ra khi có sự cố không mong muốn trong quá trình thực thi chương trình, như truy cập vào một phần tử không tồn tại trong danh sách.
