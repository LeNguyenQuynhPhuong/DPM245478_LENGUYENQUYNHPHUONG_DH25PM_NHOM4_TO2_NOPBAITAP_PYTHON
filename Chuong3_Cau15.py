'''Câu 15: Giải thích cách chạy các dòng lệnh range 
Yêu cầu: 
(a) range(5) 
(b) range(5, 10) 
(c) range(5, 20, 3) 
(d) range(20, 5, -1) 
(e) range(20, 5, -3) 
(f) range(10, 5) 
(g) range(0) 
(h) range(10, 101, 10) 
(i) range(10, -1, -1) 
(j) range(-3, 4) 
(k) range(0, 10, 1)'''

# Giải thích cách chạy các dòng lệnh range
# (a) range(5) : Tạo ra một dãy số từ 0 đến 4
# (b) range(5, 10) : Tạo ra một dãy số từ 5 đến 9
# (c) range(5, 20, 3) : Tạo ra một dãy số từ 5 đến 19, bước nhảy là 3
# (d) range(20, 5, -1) : Tạo ra một dãy số từ 20 đến 6, bước nhảy là -1
# (e) range(20, 5, -3) : Tạo ra một dãy số từ 20 đến 8, bước nhảy là -3
# (f) range(10, 5) : Không tạo ra giá trị nào vì bắt đầu lớn hơn kết thúc
# (g) range(0) : Không tạo ra giá trị nào
# (h) range(10, 101, 10) : Tạo ra một dãy số từ 10 đến 100, bước nhảy là 10
# (i) range(10, -1, -1) : Tạo ra một dãy số từ 10 đến 0, bước nhảy là -1
# (j) range(-3, 4) : Tạo ra một dãy số từ -3 đến 3
# (k) range(0, 10, 1) : Tạo ra một dãy số từ 0 đến 9, bước nhảy là 1